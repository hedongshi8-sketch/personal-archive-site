import fs from "node:fs";
import path from "node:path";

const root = process.cwd();

function read(relativePath) {
  return fs.readFileSync(path.join(root, relativePath), "utf8").trim();
}

const sections = [
  {
    title: "1. Upgrade account, editing, comments, owner updates, and storage policies",
    file: "supabase/migrations/20260619_account_editing.sql",
  },
  {
    title: "2. Hide legacy anonymous test comments",
    file: "supabase/migrations/20260620_hide_legacy_anonymous_comments.sql",
  },
  {
    title: "3. Hide internal portfolio assembly notes",
    file: "supabase/migrations/20260620_hide_internal_portfolio_items.sql",
  },
  {
    title: "4. Mark the registered site owner account as owner",
    file: "supabase/set-owner.sql",
  },
  {
    title: "5. Show post-upgrade database checks",
    file: "supabase/account-editing-check.sql",
  },
];

const output = [
  "-- Generated by scripts/compose-supabase-upgrade-sql.mjs.",
  "-- Paste this whole output into the Supabase SQL Editor and run it once.",
  "-- If the owner check says the account is missing, register or log in on the site once, then rerun section 2.",
  "",
  ...sections.flatMap((section) => [
    "-- -----------------------------------------------------------------------------",
    `-- ${section.title}`,
    `-- Source: ${section.file}`,
    "-- -----------------------------------------------------------------------------",
    read(section.file),
    "",
  ]),
].join("\n");

process.stdout.write(`${output}\n`);
